import { ApiProperty } from '@nestjs/swagger';
import {
  IsDateString,
  IsIn,
  IsInt,
  IsNotEmpty,
  IsString,
} from 'class-validator';

export class CreateBookingDto {
  @ApiProperty({ description: 'The task id' })
  @IsNotEmpty()
  @IsInt()
  taskId: number;

  @ApiProperty({ description: 'The user id' })
  @IsNotEmpty()
  @IsInt()
  userId: number;

  @ApiProperty({ description: 'The scheduled date' })
  @IsNotEmpty()
  @IsDateString()
  scheduledDate: string;

  @ApiProperty({
    description: 'How the booking will be paid',
    enum: ['wallet', 'direct'],
  })
  @IsNotEmpty()
  @IsString()
  @IsIn(['wallet', 'direct'])
  paymentMethod: 'wallet' | 'direct';
}

export class CancelBookingDto {
  @ApiProperty({ description: 'The booking id' })
  @IsNotEmpty()
  @IsInt()
  bookingId: number;

  @ApiProperty({ description: 'The reason for cancellation' })
  @IsNotEmpty()
  @IsString()
  reason: string;
}
